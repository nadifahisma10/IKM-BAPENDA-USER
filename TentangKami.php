            2019 tentang Pembentukan, Organisasi, dan Tata Kerja Unit Pelaksana Teknis Daerah Pendapatan
            pada Badan Pendapatan Daerah Provinsi Lampung. UPTD VII meliputi Kantor Bersama Samsat Kota
            Agung Kabupaten Tanggamus, Samsat Pembantu Pringsewu, dan Samsat lainnya di wilayah Kabupaten
            Pringsewu dan Kabupaten Tanggamus.</p>
